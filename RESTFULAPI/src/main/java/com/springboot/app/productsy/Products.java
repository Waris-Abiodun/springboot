package com.springboot.app.productsy;

/**
 * 
 * products: this a getter and setter class for id and name of our products
 * this is where product values or attributes are sets and gets
 */
public class Products {
	private String id;
	private String name;
	
	public String getid() {
		return id;
	}
	
	public void setId(String id) {
		this.id = id;
	}
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}

}
